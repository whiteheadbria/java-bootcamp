class ArraysAndLoops {
    public static void main(String[] args) {
        String[] drivers = {"Lewis", "George", "Charles", "Carlos"};
        System.out.println("The number of elements is: " + drivers.length);
        for(int i = 0; i < drivers.length; i++) {
            System.out.println("The element at index " + i + " is Formula One Driver " + drivers[i]);
        }

    }
}